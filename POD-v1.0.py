# –†–∞—Å—Å—á–µ—Ç –∑–∞—Ä–ø–ª–∞—Ç—ã ( !! –ù–ï –¢–†–û–ì–ê–¢–¨ !! )
def calculate_salary(level, actions):

    norm = norms.get(level, 0)

    # –§–∏–∫—Å–∏—Ä–æ–≤–∞–Ω–Ω–∞—è –∑–∞—Ä–ø–ª–∞—Ç–∞ –º–æ–¥–µ—Ä–∞—Ü–∏–∏
    # –î–ª—è –∏–∑–º–µ–Ω–µ–Ω–∏—è: 1 —á–∏—Å–ª–æ - —É—Ä–æ–≤–µ–Ω—å, 2 —á–∏—Å–ª–æ - –∑–∞—Ä–ø–ª–∞—Ç–∞
